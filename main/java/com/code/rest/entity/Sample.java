package com.code.rest.entity;

public class Sample {
	private String from;
	private String to;
	
	public Sample() {
		super();
	}
	public Sample(String from, String to) {
		super();
		this.from = from;
		this.to = to;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	

}
